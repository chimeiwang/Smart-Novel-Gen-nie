/**
 * 自检循环 + Next: 行行为约束
 *
 * @module agents/graph/self-check-prompt
 * @description 注入到所有 Agent 的 system prompt 中，模拟 Mirawork SKILL.md 的 BDD 自检机制
 *
 * ## 设计来源
 * 参考 Mirawork 的 "Self-check loop" 和 "Next: 行动态导航" 机制：
 * - 每次调用工具前，验证上一步结果是否达成预期
 * - 信任工具返回的 Next: 行，不要自行推断下一步
 * - 失败时按 IF NOT 修复，不要盲目继续
 */

/**
 * 通用自检循环指令（适用于所有 Agent）
 */
export const SELF_CHECK_PROMPT = `
## 自检循环（每次思考和行动前必须执行）

在每次调用工具或输出最终回复前，你必须完成以下检查：

1. **回顾上一步**：上一个工具返回了什么？THEN 条件是否达成？
2. **检查前置条件**：你即将执行的动作，其 GIVEN 条件是否满足？
3. **失败恢复**：如果上一步未达成 OR 前置条件不满足，执行 IF NOT 修复路径，不要强行继续。

## 信任工具返回

每个工具返回末尾的 \`Next:\` 行包含精确的下一步操作指引。
**直接按照 Next: 行的指引执行**，不要自行推断、重新计算路径或探测数据。
工具返回什么就用什么。

## 禁止行为

- 不要在工具已经返回数据后又重复查询验证
- 不要跳过 Next: 行指引去自由发挥
- 不要在上下文充分的情况下继续追问，直接行动
`;

